#Programa para calcular quanto sera o salario depois do bonus.
#Pergunta quanto é o salario atual.
salario = float(input('Qual o salário do ficionario:'))
#Calcula o salario mais valor do bonus(20%) dividido por 100.
novo = salario + (salario * 20 / 100)
#Escreve na tela o salario e tambem o salario com o bonus.
print('Um fucionario que ganhava R${:.2f}, com 15% de aumento, passa a receber R${:.2f}' .format(salario, novo))







