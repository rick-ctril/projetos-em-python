#Conversor de temperatura de C para F.
#Nessa variavel você informar a temperatura em C.
c = float(input('Informe a temperatura em  oC: '))
#Aqui é feita a multiplicação da variavel C por 9 dividida por 5 mais 32 que sera o valor em F.
f = 9*c / 5 + 32
#Escreve na tela o resultado.
print('A temperatura de {}oC corresponde a {}oF!' .format(c, f))





